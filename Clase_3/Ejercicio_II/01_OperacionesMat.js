function restar(num1, num2){
    return num1 - num2;
}

function sumar(num1, num2){
    return num1 + num2;
}

function multiplicar(num1, num2){
    return num1 * num2;
}

function dividir(num1, num2){
    return num1 / num2
}


module.exports = {
restar: restar,
sumar: sumar,
multiplicar: multiplicar,
dividir: dividir,
}